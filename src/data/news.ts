/** 根据 slug 获取单篇新闻 */
export function getNewsBySlug(slug: string): NewsPost | undefined {
  return newsPosts.find((n) => n.slug === slug);
}

/** 根据分类获取新闻列表 */
export function getNewsByCategory(category: string): NewsPost[] {
  return newsPosts.filter((n) => n.category === category);
}
